using System;
namespace CSharpFeatures
{
    class ImplicitTypedExample
    {
        public static void Main()
        {
            // integer
            var a = 10;
            // string
            var s = "javatpoint";
            // array
            var arr = new[] { 2,5,6 };
            Console.WriteLine(a);
            Console.WriteLine(s);
            Console.WriteLine(arr[2]);
        }
    }
}
